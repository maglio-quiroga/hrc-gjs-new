    <h2 class="text-center mb-5">Hospital Amigo</h2>

    <p>
        El Hospital Amigo es una estrategia que impulsa la apertura de los establecimientos de salud a la familia y comunidad, fortaleciendo la relación del equipo de salud con los usuarios, hacia una salud amable, comprometida, acogedora y participativa. Esta iniciativa es parte de la línea programática del Ministerio de Salud y se encuentra en implementación desde el año 2006.
    </p>

    <p>
        Este modelo de atención de salud integral responde a 3 aspectos fundamentales:
    </p>

    <ol>
        <li>Tener una salud comprometida, acogedora y participativa que logre satisfacer las necesidades y expectativas de la población.</li>
        <li>Atención preferencial a los adultos mayores debido al cambio en la estructura etaria de nuestro país y al envejecimiento acelerado de la población.</li>
        <li>Atención preferencial a los niños y niñas, aportando a la disminución de las desigualdades. Contempla la aplicación de algunas medidas que son ratificadas por la Ley de Derechos y Deberes de las Personas en Salud, iniciativas orientadas a la satisfacción usuaria y otras, que se enmarcan en la decisión del sector salud de brindar acompañamiento de la familia a las personas durante su hospitalización (visita diaria, alimentación asistida, acompañamiento nocturno) o durante la realización de la procedimientos y atención, así como también; la entrega de información oportuna y de calidad sobre la situación de salud de la persona hospitalizada o en procedimientos de atención.</li>
    </ol>

    <hr>

    <h3 class="mb-3">Medidas de nuestro hospital:</h3>
    <ul>
        <li>Servicios clínicos con camas básicas permite un mínimo de seis horas de visita.</li>
        <li>Servicios clínicos con camas pediátricas permite el acompañamiento diurno y nocturno de niños y niñas.</li>
        <li>Servicios clínicos cuentan con un horario determinado para la entrega de información a familiares de personas hospitalizadas.</li>
        <li>Servicios clínicos con camas básicas de adultos permiten el acompañamiento diurno y nocturno a adultos mayores.</li>
        <li>Servicios clínicos de camas básicas adulto promueve la alimentación asistida.</li>
        <li>Servicios clínicos con camas de adultos y pediátricas cuenta con un sistema visible de identificación de las personas hospitalizadas.</li>
        <li>Permite el acompañamiento de persona significativa durante el preparto, parto y postparto.</li>
        <li>Las Unidades de Emergencia de los establecimientos de mayor complejidad cuentan con sistema de información a la familia.</li>
        <li>Servicios clínicos con camas de adulto incorporan a la familia al egreso hospitalario del adulto mayor.</li>
        <li>Funcionarios cuentan con identificación.</li>
