<div class="container we_4a">

    <div class="text-center mb-5">
        <h2 class="we_2b">Ley N°21.372: Ley Mila</h2>
    </div>

    <p class="fs-4 text-center lh-sm text-info-emphasis">
        La Ley N°21.372, conocida como Ley Mila, garantiza el derecho de los niños, niñas y adolescentes (NNA) y de las mujeres gestantes a estar acompañados por personas significativas durante su atención médica, ya sea en hospitalizaciones o en procedimientos ambulatorios.
    </p>

    
    <hr class="my-5">
        
    <div class="my-3">
        <h3 class="">¿En qué consiste?</h3>
        <hr class="mt-2 we_8a">
        <p>
            Establece como derecho el que los prestadores permitirán en todo momento la compañia a niños, niñas, adolescentes, mujeres o personas gestantes en su atención y hospitalización.
        </p>
        <p>
            Además, establece estándares especiales para el acompañamiento y dispone un mandato general de trato digno y respetuoso hacia quienes los acompañan.
        </p>
        <p>
            Su objetivo es fortalecer el apoyo emocional y físico de los pacientes en momentos críticos, reconociendo la importancia de la compañía de seres queridos durante el proceso de atención médica.
        </p>
    </div>

    <hr class="my-5">

    <div class="my-3">
        <h3 class="">¿A quiénes beneficia?</h3>
        <hr class="mt-2 we_8a">
        <ul>
            <li><p><strong>Niños, niñas y adolescentes (NNA):</strong> Desde recién nacidos hasta los 19 años, 11 meses y 29 días, tienen derecho a estar acompañados durante su hospitalización y atención ambulatoria.</p></li>
            <li><p><strong>Mujeres embarazadas y personas gestantes:</strong> Durante las etapas de preparto, parto y puerperio.</p></li>
        </ul>
    </div>

    <hr class="my-5">

    <div class="my-3">
        <h3 class="">¿Quiénes pueden acompañar a los pacientes?</h3>
        <hr class="mt-2 we_8a">
        <p>
            Los niños y niñas deben estar acompañados por un adulto responsable que tenga una relación cercana con ellos, como el padre, la madre o los tutores legales.
        </p>
        <p>
            En el caso de los adolescentes entre 15 y 19 años, pueden ser acompañados por el padre, la madre, los tutores legales,adulto responsable o incluso un adolescente de la misma edad. Se les consulta previamente si desean contar con compañía durante su estancia.
        </p>
        <p>
            Las mujeres embarazadas y personas gestantes tienen el derecho de decidir quién será la persona significativa que las acompañe.
        </p>
    </div>

    <hr class="my-5">

    <div class="my-3">
        <h3 class="">¿Cuáles son los horarios?</h3>
        <hr class="mt-2 we_8a">
        <p>
            El acompañante de niños, niñas y adolescentes está autorizado para permanecer las 24 horas del día con el paciente. Sin embargo, las visitas adicionales deben ajustarse al horario establecido por el hospital para estos fines.
        </p>
        <p>
            En el caso de mujeres embarazadas y personas gestantes, el acompañante tiene permitido permanecer durante todo el proceso de trabajo de parto, el parto mismo y el puerperio inmediato, sin importar la duración de este periodo.
        </p>
    </div>

    <hr class="my-5">

    <div class="my-3">
        <h3 class="">¿Se puede restringir el acompañamiento?</h3>
        <hr class="mt-2 we_8a">
        <p class="mb-2">
            Si, existen restricciones que se aplican en casos específicos donde la presencia del acompañante pueda representar un riesgo, vulnerar derechos, o incumplir normas legales, sanitarias o institucionales. A continuación, se detallan las situaciones en las que el acompañamiento durante la atención de un Niño, Niña o Adolescente (NNA) o una persona gestante puede ser restringido.
        </p>
        <ul class="we_8b">
            <li><strong>Riesgo para el NNA o persona gestante:</strong> Si la presencia del acompañante representa un peligro para su seguridad o bienestar.</li>
            <li><strong>Falta de consentimiento o privacidad solicitada:</strong> Cuando el paciente solicita privacidad y no autoriza el acompañamiento.</li>
            <li><strong>Condiciones de salud del acompañante:</strong> Si el acompañante se encuentra con una condición de salud que no le permite ejercer el rol de acompañante o ponga en riesgo al paciente.</li>
            <li><strong>Conducta hostil o violenta:</strong> En casos de hostilidad o violencia hacia la persona gestante, el NNA, el personal médico o del establecimiento.</li>
            <li><strong>Revelación de agresión:</strong> Si la persona manifiesta que el acompañante es su agresor.</li>
            <li><strong>Antecedentes legales:</strong> Si existen disposiciones judiciales o legales que prohíben la cercanía del acompañante al paciente.</li>
            <li><strong>Incumplimiento de normas:</strong> Cuando el acompañante no respeta las reglas del servicio clínico o el reglamento interno de la institución.</li>
            <li><strong>Confidencialidad:</strong> Si el equipo de salud requiere tratar un tema confidencial que implique la privacidad del paciente.</li>
            <li><strong>Restricciones sanitarias vigentes:</strong> En casos donde las disposiciones nacionales en materia de salud limitan el número de acompañantes.</li>
        </ul>
    </div>

