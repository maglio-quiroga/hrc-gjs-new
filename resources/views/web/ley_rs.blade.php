            <li>El o la especialista tratante se debe inscribir en el Sistema informático de la Ley Ricarte Soto.</li>
            <li>El o la especialista tratante hará la postulación de la persona beneficiaria al Sistema a través del formulario dispuesto en la plataforma del sitio electrónico del FONASA.</li>
            <li>El o la especialista tratante recibirá la respuesta del Comité de Expertos Clínicos del Prestador Aprobado o del Centro confirmador, según corresponda para la incorporación.</li>
            <li>Con la notificación positiva se activa el beneficio para que acceda a la entrega de los tratamientos.</li>
        </ol>
