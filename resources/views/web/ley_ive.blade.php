<div class="container we_4a">

    <div class="text-center mb-5">
        <h2 class="we_2b">Ley N°21.030: Interrupción Voluntaria del Embarazo</h2>
    </div>

    <p class="fs-4 text-center lh-sm text-info-emphasis">
        El Estado chileno, mediante la dictación de la Ley N° 21.030 reguló la despenalización de la interrupción voluntaria del embarazo en tres causales: peligro de vida de la mujer, inviabilidad fetal de carácter letal y embarazo por violación.
    </p>
    
    <hr class="my-5">

    <div class="my-3">
        <h3 class="">Primera causal: la mujer se encuentra en riesgo vital</h3>
        <hr class="mt-2 we_8a">
        <p class="fs-5 text-center lh-sm">
            La primera causal de la Ley N° 21.030 señala que se autoriza la interrupción voluntaria del embarazo cuando “La mujer se encuentre en riesgo vital, de modo que la interrupción del embarazo evite un peligro para su vida”.
        </p>
        <p>
            La constitución de la causal requiere el diagnóstico médico que establece que la continuidad de la gestación constituye riesgo de vida para la mujer.
        </p>
        <p>
            El riesgo vital de una mujer embarazada se puede expresar de 2 formas, riesgo vital inminente y no inminente.
        </p>

        <h4 class="mt-4 mb-2">Riesgo Vital Inminente</h4>
        <p>
            En caso de <strong>riesgo vital inminente</strong>, las acciones estarán centradas en la secuencia de reanimación, estabilización hemodinámica de la mujer para derivarla, si fuese posible, o a interrumpir el embarazo si esa fuese la herramienta idónea que le pueda salvar la vida.
        </p>
        <p>
            Un <strong>médico cirujano</strong> debe definir y dejar estipulado en la ficha clínica que se debe realizar la interrupción inmediatamente para salvar la vida de la mujer.
        </p>

        <h4 class="mt-4 mb-2">Riesgo Vital no inminente</h4>
        <p>
            Si la mujer embarazada tiene alguna condición médica (como enfermedades graves o complicaciones en el embarazo), un ginecobstetra revisará su historia clínica para evaluar el riesgo para su vida. Este riesgo puede ser causado por problemas que son comunes durante el embarazo o por enfermedades preexistentes
        </p>
        <p>
            Si la mujer tiene otras enfermedades (como cáncer o problemas de corazón) o enfermedades crónicas graves, el ginecobstetra debe consultar con un especialista en esa enfermedad para evaluar el riesgo para la vida de la mujer.
        </p>
        <h4 class="mt-4 mb-2">Aspectos a Considerar</h4>
        <ul>
            <li><strong>Diagnóstico y voluntad de la mujer:</strong> Para que la interrupción del embarazo sea posible, se necesita un diagnóstico médico que indique el riesgo para la mujer y también la voluntad de la mujer de tomar esa decisión. Esto debe quedar registrado por el médico en la ficha clínica.</li>
            <li><strong>Información y apoyo:</strong> La mujer debe ser informada de su situación de salud, de los riesgos para su vida, de las opciones de tratamiento disponibles, incluyendo la interrupción del embarazo. Esta información debe ser clara y entregada por el médico y un psicólogo, para que la mujer pueda tomar su decisión sin presión. Además, se le debe explicar la ley que permite la interrupción del embarazo en ciertas situaciones, brindándole apoyo y protección en su decisión.</li>
        </ul>
        <h4 class="mt-4 mb-2">Si la paciente decide interrumpir su embarazo:</h4>
        <ul>
            <li><strong>Firma de consentimiento:</strong> La mujer debe firmar un documento confirmando que recibió toda la información sobre la ley que permite la interrupción voluntaria del embarazo.</li>
            <li><strong>Acompañamiento:</strong> Se le ofrece a la paciente un programa de acompañamiento para darle apoyo emocional y psicológico. También se le puede proporcionar información sobre organizaciones que pueden ayudarla.</li>
        </ul>
    </div>

    <hr class="my-5">

    <div class="my-3">
        <h3 class="">Segunda causal: patología congénita adquirida o genética, incompatible con la vida extrauterina
        independiente, en todo caso de carácter letal</h3>
        <hr class="mt-2 we_8a">
        <p class="fs-5 text-center lh-sm">La Ley Nº 21.030 señala que se autoriza la interrupción voluntaria del embarazo cuando “el embrión o feto padezca una patología congénita adquirida o genética, incompatible con la vida extrauterina independiente, en todo caso de carácter letal”.</p>

        <h4 class="mt-4 mb-2">Constitución de la Causal</h4>
        <p>
            Para que se considere esta causal, se necesita que dos médicos especialistas confirmen que el feto tiene una patología congénita, genética o adquirida que es incompatible con la vida fuera del útero (es decir, que no puede sobrevivir una vez nacido). Esta patología debe ser de carácter letal.
        </p>
        <p>
            Se le debe explicar a la mujer sobre el diagnóstico y pronóstico de su salud y la del feto, así como las opciones para interrumpir el embarazo (ya sea mediante aborto, parto prematuro o parto a término). Además, se debe informar sobre las prestaciones médicas disponibles, tanto para su salud física y mental como para su familia.
        </p>
        <p>
            También se le debe explicar la Ley Nº 21.030, que despenaliza la interrupción del embarazo en estas situaciones, y se le debe aclarar que tiene derecho a decidir sobre la interrupción en cualquier momento de la gestación, sin importar la edad gestacional del feto.
        </p>
        <p>
            La decisión sobre el curso de la gestación corresponde exclusivamente a la mujer, quien tiene el derecho de decidir si interrumpe el embarazo o no.
        </p>

        <h4 class="mt-4 mb-2">Si la paciente decide interrumpir su embarazo:</h4>
        <p>
            La mujer debe firmar un documento confirmando que recibió toda la información sobre la ley.
        </p>
        <p>
            Se deberá ofrecer a la mujer, el programa de acompañamiento con que cuenta la institución de salud en que se encuentra, informándole que existen instituciones u organizaciones de la sociedad civil acreditadas que pueden otorgarlo
        </p>
    </div>


<hr class="my-5">

    <div class="my-3">
        <h3 class="">Tercera causal: embarazo resultado de una violación</h3>
        <hr class="mt-2 we_8a">
        <p class="fs-5 text-center lh-sm">La tercera causal de la Ley N° 21.030 explicita que se autoriza la interrupción voluntaria del embarazo cuando éste “Sea resultado de una violación, siempre que no hayan transcurrido más de doce semanas de gestación. Tratándose de una niña menor de 14 años, la interrupción del embarazo podrá realizarse siempre que no hayan transcurrido más de catorce semanas de gestación”.</p>

        <h4 class="mt-4 mb-2">Constitución de la Causal</h4>
        <p>
            En el marco de la tercera causal se convoca a un equipo de salud, especialmente constituido para estos efectos, que debe confirmar la concurrencia de los hechos que constituyen la causal y la edad gestacional, debiendo entregar su informe escrito en un plazo de 24 horas.
        </p>
        <p>
            Serán funciones iniciales de este equipo profesional la acogida y la evaluación de la constitución de la causal.
        </p>
        <p>
            La edad gestacional debe estar dentro de los plazos establecidos por la Ley: 12 semanas para mujeres de 14 años y más; 14 semanas para niñas menores de 14 años.
        </p>
        <p>
            Se debe informar sobre los procedimientos clínicos disponibles para realizar la interrupción del embarazo, de acuerdo con la evaluación médica, incluyendo aspectos relacionados con los posibles riesgos y complicaciones asociadas al procedimiento elegido, manejo del dolor, riesgos potenciales y posibles complicaciones.
        </p>
